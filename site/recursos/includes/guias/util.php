function digito_Modulo11($barra_numero){
   $pesos = '43290876543298765432987654329876543298765432';
   $barra_soma =0;
   for ($i = 0; $i < strlen($barra_numero); $i++) {
       $numero     = substr($barra_numero,$i,1);
       $barra_mult = substr($pesos,$i,1);
       $barra_soma = $barra_soma + ($numero * $barra_mult);
   }
   $resto = $barra_soma % 11;
   $resto = 11 - $resto;
   if (($resto == 10) || ($resto == 11)) {
       return 1;
   }	   
   else {
       return $resto;
   }	   
}

function fator_vencimento($data) {
	$data = split("/",$data);
	$ano = $data[2];
	$mes = $data[1];
	$dia = $data[0];
    return(abs((_dateToDays("1997","10","07")) - (_dateToDays($ano, $mes, $dia))));
}

function _dateToDays($year,$month,$day) {
    $century = substr($year, 0, 2);
    $year = substr($year, 2, 2);
    if ($month > 2) {
        $month -= 3;
    } else {
        $month += 9;
        if ($year) {
            $year--;
        } else {
            $year = 99;
            $century --;
        }
    }
    return ( floor((  146097 * $century)    /  4 ) +
            floor(( 1461 * $year)        /  4 ) +
            floor(( 153 * $month +  2) /  5 ) +
                $day +  1721119);
}

